SELECT A.ACNTS_INTERNAL_ACNUM,
       SUM(L.LNREPAY_AMT_RECOVERED) TOTAL_RECOVERED,
       SUM(L.LNREPAY_PRIN_ACT_RECOV) TOTAL_PRIN_RECOVERED,
       SUM(L.LNREPAY_INT_ACT_RECOV) TOTAL_IN_REOVERED,
       SUM(L.LNREPAY_CHG_ACT_RECOV) TOTAL_CHARGE_RECOVERED
  FROM LNREPAY L, ACNTS A, LNPRODPM LP
 WHERE A.ACNTS_ENTITY_NUM = 1
   AND L.LNREPAY_ENTITY_NUM = 1
   AND A.ACNTS_INTERNAL_ACNUM = L.LNREPAY_INTERNAL_ACNUM
   AND A.ACNTS_PROD_CODE = LP.LNPRD_PROD_CODE
   AND LP.LNPRD_TERM_LOAN = 1
   AND L.LNREPAY_ENTRY_DATE BETWEEN '01-JAN-2018' AND '31-JAN-2018'
  -- AND A.ACNTS_BRN_CODE = 56101
 GROUP BY A.ACNTS_INTERNAL_ACNUM;

