DECLARE
   V_COUNT    NUMBER := 0;
   V_AC_NUM   NUMBER;
BEGIN
   FOR IDX
      IN (  SELECT * FROM ACTUAL_ACCOUNT_UPDATE)
   LOOP
      IF IDX.SL = 1
      THEN
         V_AC_NUM := IDX.IACLINK_INTERNAL_ACNUM;
      END IF;

      UPDATE TRAN2015
         SET TRAN_INTERNAL_ACNUM = V_AC_NUM
       WHERE     TRAN_ENTITY_NUM = 1
             AND TRAN_ACING_BRN_CODE = IDX.IACLINK_BRN_CODE
             AND TRAN_INTERNAL_ACNUM = IDX.IACLINK_INTERNAL_ACNUM;
   END LOOP;
END; 



